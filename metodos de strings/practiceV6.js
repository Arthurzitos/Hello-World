//  trim()
// remove os espaços em branco tanto no inicio quanto no fim de uma string

// ex:

const email = "   email@example.com  "; // string com espaços em branco
const formatado = email.trim(); // removendo os espaços em branco
console.log(`_${formatado}_`); // _Utilizando para poder visualizar os espaços em branco removidos_