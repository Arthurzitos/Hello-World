// o que é uma função?

/*
    é um bloco de código que executa uma ou mais instruções
    quantas vezes forem necessárias
*/

function cumprimentar(pessoa) {
    console.log(`Olá, ${pessoa}`);
}


// passando argumentos da função
cumprimentar("Arthur");
cumprimentar("Ciclano");
cumprimentar("Fulano");

